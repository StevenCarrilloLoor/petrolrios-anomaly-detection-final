SELECT COUNT(*) AS FV_CON_NDO FROM DCTO WHERE TIP_DCTO='FV' AND NDO_DCTO IS NOT NULL AND TRIM(NDO_DCTO) <> '' AND TRIM(NDO_DCTO) <> '0';
