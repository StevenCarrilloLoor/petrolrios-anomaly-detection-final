SELECT FIRST 5 NUM_DESP, COD_PROD, NOM_PROD, CAN_DESP, VTO_DESP FROM DESP ORDER BY NUM_DESP DESC;
SELECT COUNT(*) AS COINCIDENCIAS FROM DCTO c JOIN DESP d ON TRIM(c.NDO_DCTO) = CAST(CAST(d.NUM_DESP AS BIGINT) AS VARCHAR(20)) WHERE c.TIP_DCTO='FV';
